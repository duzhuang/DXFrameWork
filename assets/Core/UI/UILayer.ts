/**
 * UI 层级枚举
 * 数值由小到大决定渲染顺序，值越小越先渲染
 */
export enum UILayer {
  /** 背景层：场景背景、地图等 */
  Background = 0,

  /** 场景层：3D/2D 场景对象、角色模型 */
  Scene = 1,

  /** 底部 UI 层：底部导航、任务栏 */
  Bottom = 2,

  /** 主界面层：主界面常驻面板 */
  Main = 3,

  /** 弹窗层：对话框、确认框、全屏面板 */
  PopUp = 4,

  /** 提示层：文字提示、Toast、气泡提示 */
  Tip = 5,

  /** 系统层：全局遮罩、Loading 动画、截断点击事件 */
  System = 6,

  /** 加载层：资源加载进度条、白屏过渡 */
  Loading = 7,
}
